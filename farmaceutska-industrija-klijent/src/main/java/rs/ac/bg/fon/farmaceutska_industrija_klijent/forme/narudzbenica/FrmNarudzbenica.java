    private void btnDodajStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajStavkuActionPerformed
        if (cmbSupstance.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Morate izabrati neku Supstancu!", "GRESKA!!!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        Supstanca supstanca = (Supstanca) cmbSupstance.getSelectedItem();

        String unetaKolicina = JOptionPane.showInputDialog(this, "Unesite kolicinu supstance " + cmbSupstance.getSelectedItem() + " koju zelite da narucite:");
        long kolicinaPoruceneSupstance = Long.parseLong(unetaKolicina);

        if (kolicinaPoruceneSupstance <= 0) {
            JOptionPane.showMessageDialog(this, "Morate uneti kolicinu koja je veca od nule!", "GRESKA!!!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        StavkaNarudzbenice postojeca = null;

        for (StavkaNarudzbenice sn : stavkeNarudzbenice) {
            if (sn.getSupstanca().getSifra().equals(supstanca.getSifra())) {
                postojeca = sn;
                break;
            }
        }

        if (postojeca != null) {
            long novaKolicina = postojeca.getKolicinaSupstance() + kolicinaPoruceneSupstance;
            postojeca.setKolicinaSupstance(novaKolicina);
            postojeca.setIznosStavke(novaKolicina * supstanca.getCena());
        } else {
            StavkaNarudzbenice nova = new StavkaNarudzbenice();
            nova.setSupstanca(supstanca);
            nova.setKolicinaSupstance(kolicinaPoruceneSupstance);
            nova.setIznosStavke(kolicinaPoruceneSupstance * supstanca.getCena());

            if (stavkeNarudzbenice.isEmpty()) {
                nova.setRedniBroj(1L);
            } else {
                nova.setRedniBroj(stavkeNarudzbenice.size() + 1L);
            }

            stavkeNarudzbenice.add(nova);
        }

        long ukupanIznos = 0;

        for (StavkaNarudzbenice stavkaNarudzbenice : stavkeNarudzbenice) {
            ukupanIznos += stavkaNarudzbenice.getIznosStavke();
        }

        txtUkupanIznos.setText(String.valueOf(ukupanIznos));

        tblStavke.setModel(new ModelStavkeNarudzbenice(stavkeNarudzbenice));
    }//GEN-LAST:event_btnDodajStavkuActionPerformed

    private void btnDodajNarudzbenicuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajNarudzbenicuActionPerformed
        if (txtSifra.getText().trim().equals("") || txtSifra.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti sifru Narudzbenice!", "GRESKA!!!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        if (cmbDobavljaci.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Morate izabrati nekog Dobavljaca!", "GRESKA!!!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        if (stavkeNarudzbenice.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti barem 1 stavku narudzbenice klikom na dugme Dodaj stavku!", "GRESKA!!!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        Narudzbenica narudzbenica = new Narudzbenica();
        narudzbenica.setSifra(Long.valueOf(txtSifra.getText().trim()));

        String datumTekst = txtDatum.getText();
        DateTimeFormatter formater = DateTimeFormatter.ofPattern("dd.MM.yyyy.");
        LocalDate datum = LocalDate.parse(datumTekst, formater);
        narudzbenica.setDatum(datum);

        narudzbenica.setUkupanIznos(Long.valueOf(txtUkupanIznos.getText()));
        narudzbenica.setKorisnik(korisnik);
        narudzbenica.setDobavljac((Dobavljac) cmbDobavljaci.getSelectedItem());
        narudzbenica.setListaStavki(stavkeNarudzbenice);

        for (StavkaNarudzbenice stavkaNarudzbenice : stavkeNarudzbenice) {
            stavkaNarudzbenice.setNarudzbenica(narudzbenica);
        }

        try {
            KontrolerKlijent.vratiInstancu().dodajNarudzbenicu(narudzbenica);
            JOptionPane.showMessageDialog(this, "Uspesno ste dodali narudzbenicu:\n" + narudzbenica + " sa stavkama:\n" + stavkeNarudzbenice);
            int izbor = JOptionPane.showConfirmDialog(this, "Da li zelite da nastavite sa dodavanjem narudzbenica?", "Dodavanje Narudzbenice", JOptionPane.YES_NO_OPTION);
            if (izbor == JOptionPane.YES_OPTION) {
                txtSifra.setText("");
                cmbSupstance.setModel(new DefaultComboBoxModel(KontrolerKlijent.vratiInstancu().prikaziSveSupstance().toArray()));
                cmbSupstance.setSelectedIndex(-1);
                txtPodaciSupstance.setText("");
                cmbDobavljaci.setSelectedIndex(-1);
                stavkeNarudzbenice = new ArrayList<>();
                tblStavke.setModel(new ModelStavkeNarudzbenice(stavkeNarudzbenice));
                txtUkupanIznos.setText("");
            } else {
                this.getTopLevelAncestor().setVisible(false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Doslo je do greske\n" + e.getMessage(), "GRESKA!!!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDodajNarudzbenicuActionPerformed


