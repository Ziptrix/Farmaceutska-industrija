    private void popuniFormu() throws Exception {
        DateTimeFormatter format = DateTimeFormatter.ofPattern("dd.MM.YYYY.");
        String danasnjiDatum = LocalDate.now().format(format);
        txtDatum.setText(danasnjiDatum);

        cmbDobavljaci.removeAllItems();
        cmbDobavljaci.setModel(new DefaultComboBoxModel(KontrolerKlijent.vratiInstancu().prikaziSveDobavljace().toArray()));
        cmbDobavljaci.setSelectedIndex(-1);

        cmbSupstance.removeAllItems();
        cmbSupstance.setModel(new DefaultComboBoxModel(KontrolerKlijent.vratiInstancu().prikaziSveSupstance().toArray()));
        cmbSupstance.setSelectedIndex(-1);

        tblStavke.setModel(new ModelStavkeNarudzbenice(new ArrayList<>()));
    }
